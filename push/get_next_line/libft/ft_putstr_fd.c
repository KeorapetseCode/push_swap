#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	char	*new_s;

	if (s)
	{
		new_s = (char*)s;
		while (*new_s != '\0')
		{
			write(fd, new_s, 1);
			new_s++;
		}
	}
}
